class Bebida{

    constructor(nombre, alcohol){

        this._nombre = nombre;
        this._nombre = nombre;

    }

    set nombre(value){
        this._nombre= value;
    }
    get nombre(){
        return this._nombre;
    }

    set alcohol(value){
        this._alcohol= value;
    }
    get alcohol(){
        return this._alcohol;
    }
}